// for loop
// while
// do-while
// enhanced forloop or foreach loop
// nested for loop
// labeled for loop
public class Loops {
    public static void main(String[] args) {
        for(int i=1; i<=5; i++){
            System.out.println(i);
        }     
        int i=1;
        while( i<=5){
            System.out.println(i);
            i++;
        }   

        int j=1;
        do{
            System.out.println(j);
            j++;
        } while( j<=5);  

        
    }
}
